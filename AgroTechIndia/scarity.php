<h1 align="center" style="color:red;"> Scarcity of capital </h1>
      <p>
      
Agriculture is an important industry and like all other industries it also requires capital. The role of capital input is becoming more and more important with the advancement of farm technology. Since the agriculturists’ capital is locked up in his lands and stocks, he is obliged to borrow money for stimulating the tempo of agricultural production.<br>

The main suppliers of money to the farmer are the money-lenders, traders and commission agents who charge high rate of interest and purchase the agricultural produce at very low price. All India Rural Credit Survey Committee showed that in 1950-51 the share of money lenders stood at as high as 68.6 per cent of the total rural credit and in 1975-76 their share declined to 43 per cent of the credit needs of the farmers.<br>

This shows that the money lender is losing ground but is still the single largest contributor of agricultural credit. Rural credit scenario has undergone a significant change and institutional agencies such as Central Cooperative Banks, State Cooperative Banks, Commercial Banks, Cooperative Credit Agencies and some Government Agencies are extending loans to farmers on easy terms.<br>
</p>
